  function getAccuracy($_5_10, $_10_15, $_15_20) {
    global $datapoints;

    $correctRSSI = 0;
    $correctRSSIKalman = 0;
    $count = 0;
    foreach ($datapoints as $datapoint) {
      $count++;

      if ($datapoint['Distance'] == guessDistance($datapoint["RSSI"],$_5_10, $_10_15, $_15_20)) {
        //Correct RSSI Guess
        $correctRSSI++;
      }

      if ($datapoint['Distance'] == guessDistance($datapoint["RSSI_Kalman"],$_5_10, $_10_15, $_15_20)) {
        //Correct RSSI_Kalman Guess
        $correctRSSIKalman++;
      }
    }

    return [
      "RSSI" => round(($correctRSSI/$count)*100, 2),
      "RSSI_Kalman" => round(($correctRSSIKalman/$count)*100, 2)
    ];
  }

  function guessDistance($RSSI, $_5_10, $_10_15, $_15_20) {
    if ($RSSI >= $_5_10) {
      return '5';
    }
    if ($RSSI >= $_10_15) {
      return '10';
    }
    if ($RSSI >= $_15_20) {
      return '15';
    }
    return '20';
  }